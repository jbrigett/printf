#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	res;

	if (n < 0)
		ft_putchar('-');
	res = (n < 0 ? -n : n);
	if (res / 10 > 0)
		ft_putnbr(res / 10);
	ft_putchar(res % 10 + '0');
}
